// vector.js

class Vector {

	// -----------------------------------------------------
	constructor(a, b) {
		if (typeof a === 'object') {
			this.x = a.x;
			this.y = a.y;
		} else {
			this.x = a || 0;
			this.y = b || 0;
		}
	}

	// -----------------------------------------------------
	set(v) {
		this.x = v.x;
		this.y = v.y;
	}

	// -----------------------------------------------------
	setX(x) {
		this.x = x;
	}

	// -----------------------------------------------------
	setY(y) {
		this.y = y;
	}

	// -----------------------------------------------------
	zero() {
		this.x = 0;
		this.y = 0;
	}

	// -----------------------------------------------------
	add(v) {
		this.x += v.x;
		this.y += v.y;
	}

	// -----------------------------------------------------
	addXY(x, y) {
		this.x += x;
		this.y += y;
	}

	// -----------------------------------------------------
	sub(v) {
		this.x -= v.x;
		this.y -= v.y;
	}

	// -----------------------------------------------------
	subXY(x, y) {
		this.x -= x;
		this.y -= y;
	}

	// -----------------------------------------------------
	mul(v) {
		this.x *= v.x;
		this.y *= v.y;
	}

	// -----------------------------------------------------
	mulXY(x, y) {
		this.x *= x;
		this.y *= y;
	}

	// -----------------------------------------------------
	div(v) {
		this.x /= v.x;
		this.y /= v.y;
	}

	// -----------------------------------------------------
	divXY(x, y) {
		this.x /= x;
		this.y /= y;
	}

}

